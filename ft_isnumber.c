#include "push_swap.h"

int		ft_isnumber(char *argv)
{
	int	i;

	i = 0;
	if (argv[0] == '-' || argv[0] == '+')
		i++;
	while (argv[i] != '\0')
	{
		if (!(ft_isdigit(argv[i])))
			return (0);
		i++;
	}
	return (1);
}
